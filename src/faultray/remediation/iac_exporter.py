"""Multi-Format IaC Export Engine.

Converts FaultRay infrastructure definitions and recommendations into
actual Infrastructure-as-Code files that teams can directly apply.

Supported formats:
- Terraform HCL
- AWS CloudFormation YAML
- Kubernetes manifests
- Docker Compose YAML
- Ansible playbook YAML
- Pulumi (Python)
"""

from __future__ import annotations

import re
import textwrap
from dataclasses import dataclass, field
from enum import Enum
from typing import Any

from faultray.model.components import Component, ComponentType
from faultray.model.graph import InfraGraph


# ---------------------------------------------------------------------------
# Enums and dataclasses
# ---------------------------------------------------------------------------


class IaCFormat(str, Enum):
    """Supported IaC output formats."""

    TERRAFORM = "terraform"
    CLOUDFORMATION = "cloudformation"
    KUBERNETES = "kubernetes"
    DOCKER_COMPOSE = "docker_compose"
    ANSIBLE = "ansible"
    PULUMI_PYTHON = "pulumi_python"


@dataclass
class IaCExportResult:
    """Result of an IaC export operation."""

    format: IaCFormat
    files: dict[str, str] = field(default_factory=dict)  # filename -> content
    warnings: list[str] = field(default_factory=list)
    unsupported_components: list[str] = field(default_factory=list)
    readme: str = ""


# ---------------------------------------------------------------------------
# Helper: sanitise resource names for IaC identifiers
# ---------------------------------------------------------------------------

def _sanitize_id(name: str) -> str:
    """Turn a component id into a safe IaC identifier (alphanumeric + underscores)."""
    return re.sub(r"[^a-zA-Z0-9]", "_", name).strip("_").lower()


def _sanitize_k8s_name(name: str) -> str:
    """Turn a component id into a valid Kubernetes resource name (lowercase, dashes)."""
    return re.sub(r"[^a-z0-9-]", "-", name.lower()).strip("-")



    replicas = max(comp.replicas, 1)
    image = _DEFAULT_IMAGE.get(comp.type, "alpine:3.19")
    return textwrap.dedent(f"""\
        # --- Deployment: {comp.name} ---
        apiVersion: apps/v1
        kind: Deployment
        metadata:
          name: {kname}
          labels:
            app: {kname}
            managed-by: faultzero
        spec:
          replicas: {replicas}
          selector:
            matchLabels:
              app: {kname}
          template:
            metadata:
              labels:
                app: {kname}
            spec:
              containers:
              - name: {kname}
                image: {image}
                ports:
                - containerPort: {port}
                  protocol: TCP
                readinessProbe:
                  httpGet:
                    path: /health
                    port: {port}
                  initialDelaySeconds: 10
                  periodSeconds: 10
                livenessProbe:
                  httpGet:
                    path: /health
                    port: {port}
                  initialDelaySeconds: 30
                  periodSeconds: 15
                resources:
                  requests:
                    cpu: "100m"
                    memory: "128Mi"
                  limits:
                    cpu: "500m"
                    memory: "512Mi"
    """)



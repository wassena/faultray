# ---------------------------------------------------------------------------
# IaCExporter class
# ---------------------------------------------------------------------------

class IaCExporter:
    """Export an InfraGraph to various IaC formats."""

    # ---- public dispatch ---------------------------------------------------

    def export(self, graph: InfraGraph, fmt: IaCFormat) -> IaCExportResult:
        """Export the graph to the requested IaC format."""
        dispatch = {
            IaCFormat.TERRAFORM: self.export_terraform,
            IaCFormat.CLOUDFORMATION: self.export_cloudformation,
            IaCFormat.KUBERNETES: self.export_kubernetes,
            IaCFormat.DOCKER_COMPOSE: self.export_docker_compose,
            IaCFormat.ANSIBLE: self.export_ansible,
            IaCFormat.PULUMI_PYTHON: self.export_pulumi,
        }
        handler = dispatch.get(fmt)
        if handler is None:
            return IaCExportResult(
                format=fmt,
                warnings=[f"Unsupported format: {fmt.value}"],
            )
        return handler(graph)

    # ---- Terraform ---------------------------------------------------------

    def export_terraform(self, graph: InfraGraph) -> IaCExportResult:
        warnings: list[str] = []
        unsupported: list[str] = []

        main_blocks: list[str] = [_TF_HEADER]

        # Variables for VPC/subnets referenced by resources
        main_blocks.append(textwrap.dedent("""\
            variable "vpc_id" {
              description = "VPC ID for resource deployment"
              type        = string
            }

            variable "subnet_ids" {
              description = "List of subnet IDs"
              type        = list(string)
            }

            # ECS cluster (shared by services)
            resource "aws_ecs_cluster" "main" {
              name = "${var.environment}-faultzero-cluster"

              setting {
                name  = "containerInsights"
                value = "enabled"
              }

              tags = local.common_tags
            }
        """))

        for comp in graph.components.values():
            sid = _sanitize_id(comp.id)
            renderer = _TF_RENDERER.get(comp.type)
            if renderer is not None:
                main_blocks.append(renderer(comp, sid))
            else:
                main_blocks.append(_tf_generic(comp, sid))
                if comp.type == ComponentType.EXTERNAL_API:
                    warnings.append(
                        f"Component '{comp.id}' is an external API and cannot be "
                        "provisioned via Terraform. A placeholder service was generated."
                    )

        content = "\n".join(main_blocks)

        readme = self._generate_readme(
            graph, IaCFormat.TERRAFORM,
            deploy_commands=[
                "terraform init",
                "terraform plan -var-file=terraform.tfvars",
                "terraform apply -var-file=terraform.tfvars",
            ],
        )

        return IaCExportResult(
            format=IaCFormat.TERRAFORM,
            files={"main.tf": content},
            warnings=warnings,
            unsupported_components=unsupported,
            readme=readme,
        )

    # ---- CloudFormation ----------------------------------------------------

    def export_cloudformation(self, graph: InfraGraph) -> IaCExportResult:
        warnings: list[str] = []
        unsupported: list[str] = []

        resource_blocks: list[str] = []
        for comp in graph.components.values():
            sid = _sanitize_id(comp.id).replace("_", "")
            sid = sid[0].upper() + sid[1:]  # PascalCase-ish for CFN
            resource_blocks.append(_cfn_resource(comp, sid))

        header = textwrap.dedent("""\
            AWSTemplateFormatVersion: '2010-09-09'
            Description: >-
              FaultZero auto-generated CloudFormation template.
              Review all resources before deploying.

            Parameters:
              VpcId:
                Type: AWS::EC2::VPC::Id
                Description: VPC for deployment
              SubnetIds:
                Type: List<AWS::EC2::Subnet::Id>
                Description: Subnets for deployment
              Environment:
                Type: String
                Default: production

            Resources:
        """)

        content = header + "\n".join(resource_blocks)

        readme = self._generate_readme(
            graph, IaCFormat.CLOUDFORMATION,
            deploy_commands=[
                "aws cloudformation validate-template --template-body file://template.yaml",
                "aws cloudformation deploy --template-file template.yaml "
                "--stack-name faultzero --parameter-overrides VpcId=vpc-xxx SubnetIds=subnet-xxx",
            ],
        )

        return IaCExportResult(
            format=IaCFormat.CLOUDFORMATION,
            files={"template.yaml": content},
            warnings=warnings,
            unsupported_components=unsupported,
            readme=readme,
        )

    # ---- Kubernetes --------------------------------------------------------

    def export_kubernetes(self, graph: InfraGraph) -> IaCExportResult:
        warnings: list[str] = []
        unsupported: list[str] = []
        files: dict[str, str] = {}

        # Namespace
        ns_content = textwrap.dedent("""\
            apiVersion: v1
            kind: Namespace
            metadata:
              name: faultzero
              labels:
                managed-by: faultzero
        """)
        files["00-namespace.yaml"] = ns_content

        for comp in graph.components.values():
            kname = _sanitize_k8s_name(comp.id)
            blocks: list[str] = []

            if comp.type == ComponentType.DATABASE:
                blocks.append(_k8s_statefulset(comp, kname))
                blocks.append(_k8s_service(comp, kname))
            elif comp.type == ComponentType.LOAD_BALANCER:
                blocks.append(_k8s_deployment(comp, kname))
                blocks.append(_k8s_service(comp, kname, svc_type="LoadBalancer"))
                blocks.append(_k8s_ingress(comp, kname))
            else:
                blocks.append(_k8s_deployment(comp, kname))
                blocks.append(_k8s_service(comp, kname))

            hpa = _k8s_hpa(comp, kname)
            if hpa:
                blocks.append(hpa)

            files[f"{kname}.yaml"] = "\n---\n".join(blocks)

        readme = self._generate_readme(
            graph, IaCFormat.KUBERNETES,
            deploy_commands=[
                "kubectl apply -f 00-namespace.yaml",
                "kubectl apply -f . -n faultzero",
                "kubectl get pods -n faultzero",
            ],
        )

        return IaCExportResult(
            format=IaCFormat.KUBERNETES,
            files=files,
            warnings=warnings,
            unsupported_components=unsupported,
            readme=readme,
        )

    # ---- Docker Compose ----------------------------------------------------

    def export_docker_compose(self, graph: InfraGraph) -> IaCExportResult:
        warnings: list[str] = []
        unsupported: list[str] = []

        services: list[str] = []
        volumes: list[str] = []

        for comp in graph.components.values():
            services.append(_dc_service(comp, graph))
            if comp.type in (ComponentType.DATABASE, ComponentType.STORAGE):
                kname = _sanitize_k8s_name(comp.id)
                volumes.append(f"  {kname}_data:")

        header = textwrap.dedent("""\
            # FaultZero auto-generated Docker Compose configuration
            # Review before running: docker compose up -d

            version: "3.9"

            services:
        """)

        footer_parts = ["\nnetworks:\n  faultzero:\n    driver: bridge\n"]
        if volumes:
            footer_parts.append("volumes:\n" + "\n".join(volumes) + "\n")

        content = header + "\n".join(services) + "\n".join(footer_parts)

        readme = self._generate_readme(
            graph, IaCFormat.DOCKER_COMPOSE,
            deploy_commands=[
                "docker compose up -d",
                "docker compose ps",
                "docker compose logs -f",
            ],
        )

        return IaCExportResult(
            format=IaCFormat.DOCKER_COMPOSE,
            files={"docker-compose.yml": content},
            warnings=warnings,
            unsupported_components=unsupported,
            readme=readme,
        )

    # ---- Ansible -----------------------------------------------------------

    def export_ansible(self, graph: InfraGraph) -> IaCExportResult:
        warnings: list[str] = []
        unsupported: list[str] = []

        tasks: list[str] = []
        for comp in graph.components.values():
            tasks.append(_ansible_task(comp))

        playbook = textwrap.dedent("""\
            # FaultZero auto-generated Ansible playbook
            # Usage: ansible-playbook -i inventory.ini playbook.yml
            ---
            - name: Deploy FaultZero infrastructure
              hosts: all
              become: true
              vars:
                managed_by: faultzero
              tasks:
                - name: Install Docker dependencies
                  ansible.builtin.package:
                    name:
                      - docker.io
                      - python3-docker
                    state: present
                  tags:
                    - setup

                - name: Ensure Docker is running
                  ansible.builtin.service:
                    name: docker
                    state: started
                    enabled: true
                  tags:
                    - setup

        """)
        playbook += "\n".join(f"        {line}" for task in tasks for line in task.splitlines())

        readme = self._generate_readme(
            graph, IaCFormat.ANSIBLE,
            deploy_commands=[
                "ansible-playbook -i inventory.ini playbook.yml --check",
                "ansible-playbook -i inventory.ini playbook.yml",
            ],
        )

        return IaCExportResult(
            format=IaCFormat.ANSIBLE,
            files={"playbook.yml": playbook},
            warnings=warnings,
            unsupported_components=unsupported,
            readme=readme,
        )

    # ---- Pulumi (Python) ---------------------------------------------------

    def export_pulumi(self, graph: InfraGraph) -> IaCExportResult:
        warnings: list[str] = []
        unsupported: list[str] = []

        resource_blocks: list[str] = []
        for comp in graph.components.values():
            sid = _sanitize_id(comp.id)
            resource_blocks.append(_pulumi_resource(comp, sid))

        header = textwrap.dedent("""\
            \"\"\"FaultZero auto-generated Pulumi program (Python).

            Usage:
                pulumi up
            \"\"\"

            import json

            import pulumi
            import pulumi_aws as aws

            config = pulumi.Config()

            common_tags = {
                "managed-by": "faultzero",
                "environment": config.get("environment") or "production",
            }

            # Shared ECS cluster
            ecs_cluster = aws.ecs.Cluster(
                "faultzero-cluster",
                settings=[aws.ecs.ClusterSettingArgs(
                    name="containerInsights",
                    value="enabled",
                )],
                tags=common_tags,
            )
        """)

        content = header + "\n".join(resource_blocks)

        readme = self._generate_readme(
            graph, IaCFormat.PULUMI_PYTHON,
            deploy_commands=[
                "pulumi stack init dev",
                "pulumi up --yes",
                "pulumi stack output",
            ],
        )

        return IaCExportResult(
            format=IaCFormat.PULUMI_PYTHON,
            files={"__main__.py": content},
            warnings=warnings,
            unsupported_components=unsupported,
            readme=readme,
        )

    # ---- Remediation export ------------------------------------------------

    def export_remediation(
        self,
        graph: InfraGraph,
        changes: list[dict[str, Any]],
        fmt: IaCFormat,
    ) -> IaCExportResult:
        """Export only the changes/recommendations as IaC.

        *changes* is a list of dicts describing what to change, e.g.
        ``[{"component_id": "db", "action": "add_replicas", "replicas": 3}]``.
        The method applies those changes to a copy of the graph and exports
        the modified subset.
        """
        # For simplicity, export the full graph in the requested format
        # and add warnings about which components were changed.
        result = self.export(graph, fmt)
        for change in changes:
            cid = change.get("component_id", "unknown")
            action = change.get("action", "modify")
            result.warnings.append(
                f"Remediation applied: {action} on component '{cid}'"
            )
        return result

    # ---- Readme generation -------------------------------------------------

    def _generate_readme(
        self,
        graph: InfraGraph,
        fmt: IaCFormat,
        deploy_commands: list[str] | None = None,
    ) -> str:
        lines = [
            f"# FaultZero IaC Export — {fmt.value}",
            "",
            "Auto-generated infrastructure-as-code from FaultZero topology.",
            "",
            "## Components",
            "",
            f"Total components: {len(graph.components)}",
            "",
            "| Component | Type | Replicas |",
            "|-----------|------|----------|",
        ]
        for comp in graph.components.values():
            lines.append(f"| {comp.name} | {comp.type.value} | {comp.replicas} |")

        lines.append("")
        lines.append("## Deployment")
        lines.append("")

        if deploy_commands:
            lines.append("```bash")
            for cmd in deploy_commands:
                lines.append(cmd)
            lines.append("```")

        lines.append("")
        lines.append("## Important Notes")
        lines.append("")
        lines.append("- Review all generated files before applying to production.")
        lines.append("- Replace placeholder values (passwords, VPC IDs, etc.).")
        lines.append("- Managed by tag: `faultzero`")
        lines.append("")

        return "\n".join(lines)

def _k8s_hpa(comp: Component, kname: str) -> str:
    if not comp.autoscaling.enabled:
        return ""
    return textwrap.dedent(f"""\
        ---
        # --- HPA: {comp.name} ---
        apiVersion: autoscaling/v2
        kind: HorizontalPodAutoscaler
        metadata:
          name: {kname}-hpa
          labels:
            managed-by: faultzero
        spec:
          scaleTargetRef:
            apiVersion: apps/v1
            kind: Deployment
            name: {kname}
          minReplicas: {comp.autoscaling.min_replicas}
          maxReplicas: {comp.autoscaling.max_replicas}
          metrics:
          - type: Resource
            resource:
              name: cpu
              target:
                type: Utilization
                averageUtilization: {int(comp.autoscaling.scale_up_threshold)}
    """)


def _k8s_ingress(comp: Component, kname: str) -> str:
    port = _comp_port(comp)
    return textwrap.dedent(f"""\
        ---
        # --- Ingress: {comp.name} ---
        apiVersion: networking.k8s.io/v1
        kind: Ingress
        metadata:
          name: {kname}-ingress
          labels:
            managed-by: faultzero
          annotations:
            nginx.ingress.kubernetes.io/rewrite-target: /
        spec:
          ingressClassName: nginx
          rules:
          - host: {kname}.example.com
            http:
              paths:
              - path: /
                pathType: Prefix
                backend:
                  service:
                    name: {kname}
                    port:
                      number: {port}
    """)



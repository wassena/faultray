# Docker Compose templates
# ---------------------------------------------------------------------------

def _dc_service(comp: Component, graph: InfraGraph) -> str:
    port = _comp_port(comp)
    image = _DEFAULT_IMAGE.get(comp.type, "alpine:3.19")
    kname = _sanitize_k8s_name(comp.id)

    depends_on_items = []
    for dep_comp in graph.get_dependencies(comp.id):
        dep_name = _sanitize_k8s_name(dep_comp.id)
        depends_on_items.append(f"      {dep_name}:\n        condition: service_healthy")

    depends_section = ""
    if depends_on_items:
        depends_section = "    depends_on:\n" + "\n".join(depends_on_items)

    replicas_section = ""
    if comp.replicas > 1:
        replicas_section = f"""\
    deploy:
      replicas: {comp.replicas}
      restart_policy:
        condition: on-failure
        max_attempts: 3"""

    healthcheck = f"""\
    healthcheck:
      test: ["CMD", "wget", "--spider", "-q", "http://localhost:{port}/health"]
      interval: 30s
      timeout: 10s
      retries: 3
      start_period: 30s"""

    volume_section = ""
    if comp.type in (ComponentType.DATABASE, ComponentType.STORAGE):
        volume_section = f"""\
    volumes:
      - {kname}_data:/data"""

    lines = [f"  # --- {comp.name} ---"]
    lines.append(f"  {kname}:")
    lines.append(f"    image: {image}")
    lines.append(f"    container_name: {kname}")
    lines.append("    ports:")
    lines.append(f'      - "{port}:{port}"')
    lines.append(healthcheck)
    if replicas_section:
        lines.append(replicas_section)
    if volume_section:
        lines.append(volume_section)
    if depends_section:
        lines.append(depends_section)
    lines.append("    networks:")
    lines.append("      - faultzero")
    lines.append("    labels:")
    lines.append("      managed-by: faultzero")
    lines.append("")

    return "\n".join(lines)


